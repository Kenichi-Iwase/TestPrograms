def greeting(home):
    if home == "アメリカ":
        print("Hello, America!")
    elif home == "日本":
        print("Hello, Japan!")
    elif home == "カナダ":
        print("Hello, Canada!")
    elif home == "タイ":
        print("Hello, Thailand!")
    elif home == "メキシコ":
        print("Hello, Mexico!")
    elif home == "ブラジル":
        print("Hello, Brazil!")
    else:
        print("Hello, world!")

greeting("アメリカ")
greeting("日本")
greeting("カナダ")
greeting("タイ")
greeting("メキシコ")
greeting("ブラジル")
greeting("ロシア")
